#include "libft.h"

void	ft_bzero(void *b, size_t n)
{
	unsigned char	*str;
	size_t			i;

	i = 0;
	str = (unsigned char *)b;
	while (i < n)
	{
		str[i] = 0;
		i++;
	}
}
