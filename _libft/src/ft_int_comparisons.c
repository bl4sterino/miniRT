/// Comparison int equals
int	ft_equals(int a, int b)
{
	return (a == b);
}

/// Comparison int different
int	ft_differs(int a, int b)
{
	return (a != b);
}

/// Comparison int greater
int	ft_greater(int a, int b)
{
	return (a > b);
}

/// Comparison int smaller
int	ft_smaller(int a, int b)
{
	return (a < b);
}
