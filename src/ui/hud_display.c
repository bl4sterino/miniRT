#include "rt.h"

void ft_hud_display(t_data *d)
{
	ft_draw_str(d, (t_draw_arg){(t_v2i){0, 0}, (t_v2i){70, 15},
		(t_rgb){50, 50, 50}}, d->fpsstr);
	ft_draw_double(d, (t_draw_arg){(t_v2i){0, 15}, (t_v2i){100, 15},
		(t_rgb){50, 50, 50}}, "loop:", ft_clock_get(1));
}
